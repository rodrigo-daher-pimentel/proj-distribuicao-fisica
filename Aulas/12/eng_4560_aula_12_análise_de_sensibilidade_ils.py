def find_file_recursive(folder, filename):
    """
    Procura um arquivo dentro da pasta, inclusive em subpastas.
    Isso ajuda caso o ZIP tenha uma pasta interna.
    """
    folder = Path(folder)

    matches = list(folder.rglob(filename))

    if len(matches) == 0:
        raise FileNotFoundError(f"Arquivo {filename} não encontrado em {folder}")

    return matches[0]


def load_instance_for_scalability(folder, solution_filename="solution_busca_local_final.json"):
    """
    Carrega uma instância completa a partir de uma pasta local.
    Não lê nada do Drive.
    """

    folder = Path(folder)

    nodes_path = find_file_recursive(folder, "nodes.csv")
    D_path = find_file_recursive(folder, "D.npy")
    q_path = find_file_recursive(folder, "q.npy")
    s_path = find_file_recursive(folder, "s.npy")
    params_path = find_file_recursive(folder, "params.json")

    nodes_i = pd.read_csv(nodes_path)
    D_i = np.load(D_path)
    q_i = np.load(q_path)
    s_i = np.load(s_path)

    cvar_matches = list(folder.rglob("Cvar.npy"))
    Cvar_i = np.load(cvar_matches[0]) if len(cvar_matches) > 0 else None

    with open(params_path, "r", encoding="utf-8") as f:
        params_raw_i = json.load(f)

    params_i = normalize_params(params_raw_i)

    # Procura a solução baseline
    solution_matches = list(folder.rglob(solution_filename))

    if len(solution_matches) == 0:
        # Se não encontrar exatamente esse nome, pega qualquer JSON que não seja params.json
        json_candidates = [
            p for p in folder.rglob("*.json")
            if p.name != "params.json"
        ]

        if len(json_candidates) == 0:
            raise FileNotFoundError(
                f"Nenhum JSON de solução encontrado em {folder}."
            )

        solution_path = json_candidates[0]

    else:
        solution_path = solution_matches[0]

    with open(solution_path, "r", encoding="utf-8") as f:
        sol_json_i = json.load(f)

    if isinstance(sol_json_i, dict) and "solution" in sol_json_i:
        sol_i = sol_json_i["solution"]
    else:
        sol_i = sol_json_i

    sol_i = normalize_solution(sol_i)

    return {
        "folder": str(folder),
        "nodes": nodes_i,
        "D": D_i,
        "Cvar": Cvar_i,
        "q": q_i,
        "s": s_i,
        "params": params_i,
        "baseline_solution": sol_i,
        "solution_file": str(solution_path)
    }

