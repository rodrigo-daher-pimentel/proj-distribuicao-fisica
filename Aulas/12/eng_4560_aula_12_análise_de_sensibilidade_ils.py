def accept_solution(cost_new, cost_current, criterion="strict", tolerance_pct=0.03):
    if cost_new < cost_current - 1e-6:
        return True

    if criterion == "strict":
        return False

    elif criterion == "tolerance":
        return cost_new <= cost_current * (1.0 + tolerance_pct)

    else:
        raise ValueError(f"Critério inválido: {criterion}")

def iterated_local_search(initial_solution,
                          D, q, s, params,
                          n_iterations=100,
                          seed=42,
                          perturbation_type="relocate_random",
                          perturbation_k=1,
                          accept_criterion="strict",
                          tolerance_pct=0.03,
                          use_2opt=True,
                          use_relocate=True,
                          use_swap=False,
                          verbose=True):

    rng = random.Random(seed)
    start_time = time.time()

    current_solution = local_search(
        initial_solution, D, q, s, params,
        use_2opt=use_2opt,
        use_relocate=use_relocate,
        use_swap=use_swap
    )

    current_cost = total_solution_cost(current_solution, D, params)

    best_solution = copy.deepcopy(current_solution)
    best_cost = current_cost

    history = [{
        "iteration": 0,
        "current_cost": current_cost,
        "best_cost": best_cost,
        "accepted": True,
        "elapsed_sec": 0.0,
        "perturbation_type": perturbation_type,
        "accept_criterion": accept_criterion
    }]

    for it in range(1, n_iterations + 1):
        perturbed = perturb_solution(
            current_solution,
            D, q, s, params,
            rng=rng,
            perturbation_type=perturbation_type,
            perturbation_k=perturbation_k
        )

        improved = local_search(
            perturbed, D, q, s, params,
            use_2opt=use_2opt,
            use_relocate=use_relocate,
            use_swap=use_swap
        )

        candidate_cost = total_solution_cost(improved, D, params)

        accepted = accept_solution(
            cost_new=candidate_cost,
            cost_current=current_cost,
            criterion=accept_criterion,
            tolerance_pct=tolerance_pct
        )

        if accepted:
            current_solution = copy.deepcopy(improved)
            current_cost = candidate_cost

        if candidate_cost < best_cost - 1e-6:
            best_solution = copy.deepcopy(improved)
            best_cost = candidate_cost

        history.append({
            "iteration": it,
            "current_cost": current_cost,
            "best_cost": best_cost,
            "accepted": accepted,
            "elapsed_sec": time.time() - start_time,
            "perturbation_type": perturbation_type,
            "accept_criterion": accept_criterion
        })

        if verbose and it % 10 == 0:
            print(f"Iteração {it}: atual={current_cost:.2f}, melhor={best_cost:.2f}")

    return best_solution, history

