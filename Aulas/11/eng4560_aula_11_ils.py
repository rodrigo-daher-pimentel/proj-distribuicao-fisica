"""## Seção 4 — Busca local como intensificação

No ILS, a busca local cumpre o papel de **intensificação**: partindo de qualquer
ponto do espaço de soluções, ela o conduz ao ótimo local mais próximo.

Isso acontece duas vezes em cada iteração do ILS:
1. Uma vez antes do laço principal (para partir de um ótimo local bem definido).
2. Uma vez após cada perturbação (para explorar o ótimo local da nova região).

Reutilizamos os três movimentos da Sprint 2:

| Movimento | Ação | Escopo |
|---|---|---|
| **2-opt** | Inverte um segmento da rota | Intra-rota |
| **Relocate** | Move um cliente para outra rota | Inter-rotas |
| **Swap** | Troca dois clientes entre rotas | Inter-rotas |

A função `local_search()` combina os três com flags booleanas.

"""

# ============================================================
# (12) MOVIMENTO 2-OPT
# ============================================================
# Inverte o segmento entre as posições i e k (inclusive).
# Exemplo: [0,A,B,C,D,0] com i=1, k=3 → [0,C,B,A,D,0]

def two_opt_swap(route, i, k):
    """Aplica um movimento 2-opt: inverte route[i:k+1]."""
    return route[:i] + route[i:k+1][::-1] + route[k+1:]


def improve_route_2opt(route, vehicle_type, D, q, s, params):
    """
    Aplica 2-opt exaustivo a uma única rota.
    Aceita o primeiro par (i,k) que reduza a distância e seja viável.
    Repete até não haver mais melhoria.
    """
    best_route = route[:]
    best_dist  = route_distance(best_route, D)
    improved   = True

    while improved:
        improved = False
        for i in range(1, len(best_route) - 2):
            for k in range(i + 1, len(best_route) - 1):
                candidate = two_opt_swap(best_route, i, k)
                if not is_route_feasible(candidate, vehicle_type, D, q, s, params):
                    continue
                cand_dist = route_distance(candidate, D)
                if cand_dist < best_dist - 1e-6:
                    best_route = candidate
                    best_dist  = cand_dist
                    improved   = True
                    break
            if improved:
                break

    return best_route


def apply_2opt(solution, D, q, s, params):
    """Aplica improve_route_2opt a cada rota da solução."""
    return [
        {**item, "route": improve_route_2opt(
            item["route"], item["vehicle"], D, q, s, params)}
        for item in solution
    ]

print("2-opt definido.")

# ============================================================
# (13) MOVIMENTO RELOCATE
# ============================================================
# Move um cliente de uma rota para outra (first improvement).

def relocate_customer(route_from, route_to, idx_from, idx_to):
    """
    Remove o cliente em idx_from de route_from e
    insere em idx_to de route_to.
    Retorna (nova_route_from, nova_route_to).
    """
    new_from = route_from[:]
    new_to   = route_to[:]
    customer = new_from.pop(idx_from)
    new_to.insert(idx_to, customer)
    return new_from, new_to


def apply_relocate(solution, D, q, s, params):
    """
    Relocate first-improvement entre todos os pares de rotas.
    Para quando encontra o primeiro movimento que reduz o custo total.
    """
    best      = copy.deepcopy(solution)
    best_cost = total_solution_cost(best, D, params)
    improved  = True

    while improved:
        improved = False
        for a in range(len(best)):
            if improved: break
            for b in range(len(best)):
                if improved or a == b: break
                r_from, r_to = best[a]["route"], best[b]["route"]
                for idx_from in range(1, len(r_from) - 1):
                    if improved: break
                    for idx_to in range(1, len(r_to)):
                        cand = copy.deepcopy(best)
                        nf, nt = relocate_customer(
                            cand[a]["route"], cand[b]["route"],
                            idx_from, idx_to)
                        cand[a]["route"] = nf
                        cand[b]["route"] = nt
                        # Remove rotas que ficaram sem clientes
                        cand = [item for item in cand
                                if any(node != 0 for node in item["route"])]
                        for rid, item in enumerate(cand, 1):
                            item["route_id"] = rid
                        if not is_solution_feasible(cand, D, q, s, params):
                            continue
                        cand_cost = total_solution_cost(cand, D, params)
                        if cand_cost < best_cost - 1e-6:
                            best, best_cost = cand, cand_cost
                            improved = True
                            break
    return best

print("Relocate definido.")

# ============================================================
# (14) MOVIMENTO SWAP
# ============================================================
# Troca dois clientes entre rotas distintas (first improvement).

def swap_customers(route_a, route_b, idx_a, idx_b):
    """
    Troca o cliente em idx_a de route_a pelo cliente em idx_b de route_b.
    Retorna (nova_route_a, nova_route_b).
    """
    new_a, new_b = route_a[:], route_b[:]
    new_a[idx_a], new_b[idx_b] = new_b[idx_b], new_a[idx_a]
    return new_a, new_b


def apply_swap(solution, D, q, s, params):
    """Swap first-improvement entre pares de rotas distintas."""
    best      = copy.deepcopy(solution)
    best_cost = total_solution_cost(best, D, params)
    improved  = True

    while improved:
        improved = False
        for a in range(len(best)):
            if improved: break
            for b in range(a + 1, len(best)):
                if improved: break
                r_a, r_b = best[a]["route"], best[b]["route"]
                for ia in range(1, len(r_a) - 1):
                    if improved: break
                    for ib in range(1, len(r_b) - 1):
                        cand = copy.deepcopy(best)
                        na, nb = swap_customers(
                            cand[a]["route"], cand[b]["route"], ia, ib)
                        cand[a]["route"] = na
                        cand[b]["route"] = nb
                        if not is_solution_feasible(cand, D, q, s, params):
                            continue
                        cand_cost = total_solution_cost(cand, D, params)
                        if cand_cost < best_cost - 1e-6:
                            best, best_cost = cand, cand_cost
                            improved = True
                            break
    return best

print("Swap definido.")

# ============================================================
# (15) BUSCA LOCAL COMPOSTA
# ============================================================
# Aplica os movimentos em sequência: 2-opt → Relocate → Swap.
# Cada operador pode ser ligado/desligado individualmente.

def local_search(solution, D, q, s, params,
                 use_2opt=True, use_relocate=True, use_swap=False):
    """
    Busca local composta.

    Parâmetros
    ----------
    use_2opt    : aplica 2-opt em cada rota
    use_relocate: aplica relocate entre rotas
    use_swap    : aplica swap entre rotas (desativado por padrão — mais lento)
    """
    current = copy.deepcopy(solution)

    if use_2opt:
        current = apply_2opt(current, D, q, s, params)

    if use_relocate:
        current = apply_relocate(current, D, q, s, params)

    if use_swap:
        current = apply_swap(current, D, q, s, params)

    return current

print("Busca local composta definida.")

