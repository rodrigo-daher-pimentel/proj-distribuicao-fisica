"""## Seção 8 — Critério de aceitação

Após a perturbação + busca local, o ILS obtém uma nova solução *S''\**.
O **critério de aceitação** decide se *S''\** substitui a solução corrente *S\** para a
próxima iteração.

Dois critérios são implementados:

### Critério estrito (*strict*)
Aceita *S''\** apenas se for **estritamente melhor** que *S\* (custo menor).
Conservador — o algoritmo sempre trabalha a partir da melhor solução já encontrada.

### Critério com tolerância (*tolerance*)
Aceita *S''\** se o custo não exceder o custo corrente em mais de δ%.
Com δ=3%, aceita qualquer solução com custo ≤ custo_atual × 1.03.
Permite "subir pequenas colinas" para explorar regiões novas.

> ⚠️ **Isso não é Simulated Annealing.** No SA, a probabilidade de aceitar soluções piores
> diminui ao longo do tempo. Aqui, δ é **constante** durante toda a execução — é uma
> aceitação determinística com tolerância fixa.

"""

# ============================================================
# (21) CRITÉRIO DE ACEITAÇÃO   ← TODO 1
# ============================================================

def accept_solution(cost_new, cost_current, criterion, tolerance_pct=0.03):
    """
    Retorna True se a nova solução deve ser aceita como corrente.

    Parâmetros
    ----------
    cost_new      : custo da solução candidata (após perturbação + BL)
    cost_current  : custo da solução corrente
    criterion     : 'strict' ou 'tolerance'
    tolerance_pct : percentual de tolerância (ex: 0.03 para 3%)
    """

    if criterion == 'strict':
        return cost_new < cost_current - 1e-6

    elif criterion == 'tolerance':
        threshold = cost_current * (1.0 + tolerance_pct)
        return cost_new <= threshold

    else:
        raise ValueError(
            f"Critério inválido: '{criterion}'. Use 'strict' ou 'tolerance'."
        )


