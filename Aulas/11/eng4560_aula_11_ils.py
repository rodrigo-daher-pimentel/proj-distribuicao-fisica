print("  tolerance, piora 5% :", accept_solution(1050.0, 1000.0, 'tolerance', 0.03))

"""## Seção 9 — ILS completo

A função `iterated_local_search()` integra todos os componentes anteriores.

Ela retorna dois objetos:
1. **`best_solution`** — a melhor solução encontrada ao longo de *todas* as iterações.
2. **`history`** — lista de dicionários com custo corrente, melhor custo e tempo
   a cada iteração. É o que gera a curva de convergência.

> 💡 **Distinção importante:**
> - **Solução corrente**: de onde a próxima perturbação parte. Pode piorar com tolerância.
> - **Melhor solução global**: melhor já encontrada em qualquer iteração. **Nunca piora.**

"""

# ============================================================
# (22) ITERATED LOCAL SEARCH   ← TODO 2 e 3
# ============================================================

def iterated_local_search(
    initial_solution,
    D, q, s, params,
    # ── Perturbação ──────────────────────────────────────────
    perturbation_type = "relocate_random",
    perturbation_k    = 2,
    # ── Busca local ──────────────────────────────────────────
    use_2opt          = True,
    use_relocate      = True,
    use_swap          = False,
    # ── Aceitação ────────────────────────────────────────────
    accept_criterion  = "strict",
    tolerance_pct     = 0.03,
    # ── Parada ───────────────────────────────────────────────
    n_iterations      = 100,
    time_limit_sec    = None,
    # ── Misc ─────────────────────────────────────────────────
    seed              = 42,
    verbose           = True
):
    """
    Iterated Local Search para o CVRP da Prolog.

    Retorna (best_solution, history).
    """
    rng        = random.Random(seed)
    start_time = time.time()

    # ── Passo 1: busca local na solução inicial ───────────────
    current_solution = local_search(
        initial_solution, D, q, s, params,
        use_2opt, use_relocate, use_swap
    )
    current_cost = total_solution_cost(current_solution, D, params)

    best_solution = copy.deepcopy(current_solution)
    best_cost     = current_cost

    # ── Registro da iteração 0 (busca local inicial) ─────────
    history = [{
        "iteration"   : 0,
        "current_cost": current_cost,
        "best_cost"   : best_cost,
        "elapsed_sec" : 0.0,
        "accepted"    : True,          # a BL inicial é sempre aceita
    }]

    if verbose:
        print(f"{'Iter':>5} | {'Custo atual':>12} | {'Melhor':>12} | Aceito")
        print("-" * 50)
        print(f"{'0':>5} | R$ {current_cost:>9.2f} | R$ {best_cost:>9.2f} |  init")

    # ── Laço principal ────────────────────────────────────────
    for it in range(1, n_iterations + 1):

        # Verificação do limite de tempo
        elapsed = time.time() - start_time
        if time_limit_sec is not None and elapsed >= time_limit_sec:
            if verbose:
                print(f"\nLimite de tempo atingido ({elapsed:.1f}s) após {it-1} iterações.")
            break

        # ── Passo 2: perturbação ──────────────────────────────
        perturbed = perturb_solution(
            current_solution, D, q, s, params, rng,
            perturbation_type=perturbation_type,
            perturbation_k=perturbation_k
        )

        # ── Passo 3: busca local após perturbação ─────────────
        candidate      = local_search(perturbed, D, q, s, params,
                                      use_2opt, use_relocate, use_swap)
        candidate_cost = total_solution_cost(candidate, D, params)

        # ── TODO (ALUNO): Passos 4 e 5 ───────────────────────
        #
        # PASSO 4 — Critério de aceitação:
        #   Chame accept_solution com os argumentos corretos.
        #   Se aceito, atualize current_solution e current_cost.
        #
        # PASSO 5 — Atualização da melhor solução global:
        #   Se candidate_cost < best_cost, atualize best_solution e best_cost.
        #   Note: a atualização da melhor global é INDEPENDENTE da aceitação.
        #   (Uma solução pode ser o melhor já visto mesmo não sendo aceita como corrente.)
        #
        # Dica: use copy.deepcopy ao guardar a melhor solução.
        # ─────────────────────────────────────────────────────

        accepted = accept_solution(candidate_cost, current_cost,
                                  accept_criterion, tolerance_pct)
        if accepted:
             current_solution = copy.deepcopy(candidate)
             current_cost     = candidate_cost

        if candidate_cost < best_cost - 1e-6:
             best_solution = copy.deepcopy(candidate)
             best_cost     = candidate_cost

        # ── Registro do histórico ─────────────────────────────
        history.append({
            "iteration"   : it,
            "current_cost": current_cost,
            "best_cost"   : best_cost,
            "elapsed_sec" : time.time() - start_time,
            "accepted"    : accepted,
        })

        if verbose and (it <= 5 or it % 10 == 0):
            status = "✓" if accepted else "–"
            print(f"{it:>5} | R$ {current_cost:>9.2f} | R$ {best_cost:>9.2f} |  {status}")

    t_total = time.time() - start_time
    if verbose:
        ganho = (history[0]["current_cost"] - best_cost) / history[0]["current_cost"] * 100
        print("-" * 50)
        print(f"Concluído em {t_total:.1f}s | Melhor: R$ {best_cost:.2f} | "
              f"Ganho vs. BL inicial: {ganho:.1f}%")

    return best_solution, history

