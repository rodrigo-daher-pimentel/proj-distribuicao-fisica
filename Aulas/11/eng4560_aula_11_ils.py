"""## Seção 6 — O problema do ótimo local e a ideia do ILS

Após a busca local, chegamos a um **ótimo local** *S\**: uma solução tal que nenhum
movimento da vizinhança consegue melhorá-la. O algoritmo para — não porque encontrou
a melhor solução possível, mas porque ficou preso em um vale local do espaço de busca.

### Por que a busca local para onde para

Imagine o espaço de soluções como uma paisagem onde a altitude representa o custo.
A busca local é um caminhante que **sempre desce ladeira abaixo** e para ao chegar
a um vale. Esse vale pode ser raso — o vale mais profundo (ótimo global) pode estar
do outro lado de uma colina que o caminhante nunca subirá, pois a busca local nunca
aceita movimentos que piorem a solução.

### A estratégia do ILS

O **Iterated Local Search** resolve isso com um ciclo de cinco passos:

```
┌─────────────────────────────────────────────────────────┐
│  FLUXO DO ILS                                           │
│                                                         │
│  S₀ (solução inicial)                                  │
│       │                                                 │
│       ▼                                                 │
│  Busca local → S* (ótimo local)   ◄──────────┐         │
│       │                                       │         │
│       ▼                                       │         │
│  Perturbação(S*) → S'   ← diversificação      │         │
│       │                                       │         │
│       ▼                                       │         │
│  Busca local(S') → S''* ← intensificação      │         │
│       │                                       │         │
│       ▼                                       │         │
│  Critério de aceitação:                       │         │
│    aceita S''* como novo S*? ─────────────────┘         │
│       │                                                 │
│       ▼                                                 │
│  Atualiza melhor solução global se necessário           │
│       │                                                 │
│       ▼                                                 │
│  Repetir N iterações ou até limite de tempo             │
└─────────────────────────────────────────────────────────┘
```

A perturbação deve ser **forte o suficiente para sair do ótimo local atual**,
mas **fraca o suficiente para preservar estrutura da solução**. Esse equilíbrio
é o que diferencia uma boa perturbação de um reinício aleatório.

## Seção 7 — Perturbações

Implementamos três tipos de perturbação:

### P1 — Relocate aleatório
Move um cliente escolhido aleatoriamente para uma rota e posição aleatórias.
Não busca o melhor movimento — apenas um movimento aleatório válido.
Simples e rápido, mas pode não escapar de ótimos locais profundos.

### P2 — Swap aleatório
Troca dois clientes escolhidos aleatoriamente entre rotas distintas.
Mantém o número de clientes por rota aproximadamente estável.

### P3 — Double-bridge
A perturbação clássica para ILS em roteirização. Divide uma rota em 4 segmentos
e os reconecta em uma ordem que **nenhum movimento 2-opt consegue desfazer**.

Exemplo com clientes [A, B, C, D, E, F] e cortes em 1, 3, 5:
- seg1=[A], seg2=[B,C], seg3=[D,E], seg4=[F]
- Reconexão: seg1 + **seg3** + **seg2** + seg4 = [A, D, E, B, C, F]

Essa propriedade garante que a busca local após a double-bridge explore uma
**bacia de atração genuinamente diferente** da anterior.
"""

# ============================================================
# (17) PERTURBAÇÃO P1: RELOCATE ALEATÓRIO
# ============================================================

def perturb_relocate_random(solution, D, q, s, params, rng):
    """
    Move um cliente aleatório para uma rota e posição aleatórias.
    Retorna a solução perturbada se viável, ou a original se não.
    """
    candidate = copy.deepcopy(solution)

    # Rotas com pelo menos 1 cliente removível (tamanho > 2, pois [0,cliente,0])
    src_candidates = [
        idx for idx, item in enumerate(candidate)
        if len(item["route"]) > 3
    ]
    if not src_candidates:
        return candidate

    a   = rng.choice(src_candidates)
    b   = rng.randint(0, len(candidate) - 1)

    r_from = candidate[a]["route"]
    r_to   = candidate[b]["route"]

    if len(r_from) <= 2:
        return candidate

    idx_from = rng.randint(1, len(r_from) - 2)
    idx_to   = rng.randint(1, len(r_to) - 1)

    new_from, new_to = relocate_customer(r_from, r_to, idx_from, idx_to)
    candidate[a]["route"] = new_from
    candidate[b]["route"] = new_to

    # Remove rotas que ficaram sem clientes
    candidate = [item for item in candidate
                 if any(node != 0 for node in item["route"])]
    for rid, item in enumerate(candidate, 1):
        item["route_id"] = rid

    if is_solution_feasible(candidate, D, q, s, params):
        return candidate
    return copy.deepcopy(solution)   # fallback: retorna original se inviável


print("P1 (Relocate aleatório) definida.")

# ============================================================
# (18) PERTURBAÇÃO P2: SWAP ALEATÓRIO
# ============================================================

def perturb_swap_random(solution, D, q, s, params, rng):
    """
    Troca dois clientes aleatórios entre rotas distintas.
    Retorna a solução perturbada se viável, ou a original se não.
    """
    candidate = copy.deepcopy(solution)

    valid = [idx for idx, item in enumerate(candidate)
             if len(item["route"]) > 3]

    if len(valid) < 2:
        return candidate

    a, b = rng.sample(valid, 2)
    r_a  = candidate[a]["route"]
    r_b  = candidate[b]["route"]

    idx_a = rng.randint(1, len(r_a) - 2)
    idx_b = rng.randint(1, len(r_b) - 2)

    new_a, new_b = swap_customers(r_a, r_b, idx_a, idx_b)
    candidate[a]["route"] = new_a
    candidate[b]["route"] = new_b

    if is_solution_feasible(candidate, D, q, s, params):
        return candidate
    return copy.deepcopy(solution)


print("P2 (Swap aleatório) definida.")

# ============================================================
# (19) PERTURBAÇÃO P3: DOUBLE-BRIDGE
# ============================================================
# Divide os clientes de uma rota em 4 segmentos (A|B|C|D) e
# reconecta como A + C + B + D — operação não-reversível por 2-opt.
#
# Requer rota com pelo menos 4 clientes.
# Se nenhuma rota atender esse critério, usa P1 como fallback.

def perturb_double_bridge(solution, D, q, s, params, rng):
    """
    Double-bridge intra-rota.
    Retorna a solução perturbada se viável, ou a original se não.
    """
    candidate = copy.deepcopy(solution)

    # Rotas com >= 4 clientes (len(route) >= 6 contando os dois depósitos)
    valid = [idx for idx, item in enumerate(candidate)
             if len(item["route"]) >= 6]

    if not valid:
        # Fallback: usa relocate aleatório se nenhuma rota for longa o suficiente
        return perturb_relocate_random(solution, D, q, s, params, rng)

    r_idx   = rng.choice(valid)
    route   = candidate[r_idx]["route"]
    clients = route[1:-1]          # remove os dois depósitos
    n       = len(clients)

    # Escolhe 3 pontos de corte distintos em [1, n-1]
    if n < 4:
        return perturb_relocate_random(solution, D, q, s, params, rng)

    cuts      = sorted(rng.sample(range(1, n), 3))
    a, b, c   = cuts
    seg1, seg2, seg3, seg4 = clients[:a], clients[a:b], clients[b:c], clients[c:]

    # Reconexão double-bridge: seg1 + seg3 + seg2 + seg4
    new_route = [0] + seg1 + seg3 + seg2 + seg4 + [0]
    candidate[r_idx]["route"] = new_route

    if is_solution_feasible(candidate, D, q, s, params):
        return candidate
    return copy.deepcopy(solution)


print("P3 (Double-bridge) definida.")

