def total_solution_cost(solution, D, params):
    """Soma dos custos de todas as rotas (R$)."""
    return sum(route_cost(item["route"], item["vehicle"], D, params)
               for item in solution)


def solution_metrics(solution, D, q, s, params):
    """Dicionário com métricas consolidadas da solução."""
    n_fio = n_vuc = 0
    total_dist = total_cost = total_time = 0.0
    cap_viol = time_viol = 0

    for item in solution:
        route, veh = item["route"], item["vehicle"]
        dist  = route_distance(route, D)
        cost  = route_cost(route, veh, D, params)
        t, _, _ = route_total_time(route, D, s, params["speed_kmh"])

        total_dist += dist
        total_cost += cost
        total_time += t

        if veh == "FIO": n_fio += 1
        else:            n_vuc += 1

        if route_load(route, q) > params["capacity"][veh] + 1e-6:
            cap_viol += 1
        if t > params["max_hours"] + 1e-6:
            time_viol += 1

    return {
        "n_routes"           : len(solution),
        "n_fio"              : n_fio,
        "n_vuc"              : n_vuc,
        "total_distance_km"  : round(total_dist, 2),
        "total_cost_rs"      : round(total_cost, 2),
        "total_time_h"       : round(total_time, 2),
        "capacity_violations": cap_viol,
        "time_violations"    : time_viol,
    }


def build_solution_summary(solution, D, q, s, params):
    """DataFrame com uma linha por rota."""
    rows = []
    for item in solution:
        route, veh = item["route"], item["vehicle"]
        t, t_mov, t_serv = route_total_time(route, D, s, params["speed_kmh"])
        rows.append({
            "route_id"      : item["route_id"],
            "vehicle"       : veh,
            "n_clients"     : len([n for n in route if n != 0]),
            "load_kg"       : round(route_load(route, q), 2),
            "distance_km"   : round(route_distance(route, D), 2),
            "total_time_h"  : round(t, 2),
            "travel_time_h" : round(t_mov, 2),
            "service_time_h": round(t_serv, 2),
            "cost_rs"       : round(route_cost(route, veh, D, params), 2),
            "violates_H"    : t > params["max_hours"] + 1e-6,
        })
    return pd.DataFrame(rows)


